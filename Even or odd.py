print ("Check if a number is even or odd")
user_input = int(input("Enter a number: "))
if user_input%2==0:
    print(f"Your entered number {user_input} is Even")
elif user_input == 0:
    print(f"Your entered number {user_input} is Even")
else:
    print(f"Your entered number {user_input} is Odd")
